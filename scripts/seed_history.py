applied = []

def inject(html, var_name, obj):
    new_json = json.dumps(obj, separators=(', ', ':'))
    pattern = rf'const {var_name}\s*=\s*\{{[\s\S]*?\}};'
    new_html, n = re.subn(pattern, f'const {var_name} = {new_json};', html, count=1)
    if n:
        applied.append(f'{var_name} ({len(obj.get("labels",[]))} pts)')
        return new_html
    print(f'  WARNING: {var_name} not matched')
    return html


